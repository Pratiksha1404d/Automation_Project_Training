package com.JavaBasics;

public class IfElse {

	public static void main(String[] args) {
		
		int M=97;
		
		if (M>100)
		{
			System.out.println("Student is passed");
		}
		
		else if (M<99)
		{
			System.out.println("Student is average");
		}
		else
		{
			System.out.println("Student is failed");
		}
	}

}
